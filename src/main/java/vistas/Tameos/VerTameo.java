    private void buscarYMostrarTameo(int id) {
        ControladorTameos controlador1 = new ControladorTameos();
        Tameos tameo = controlador1.buscarTameoPorId(id);
        if (tameo != null) {
            dinosaurio.setText(tameo.getDinosaurio().toString().trim());
            metodoUsado.setText(tameo.getMetodo_Usado());
            horas.setText(String.valueOf(tameo.getTiempo()));
        } else {
            JOptionPane.showMessageDialog(this, "No se encontr√≥ un dinosaurio con ese nombre.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

